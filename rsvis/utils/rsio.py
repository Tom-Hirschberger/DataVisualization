from rsvis.__init__ import _logger
import rsvis.utils.imgio 

#   class -------------------------------------------------------------------
# ---------------------------------------------------------------------------
class RSIO(Object):

    #   method --------------------------------------------------------------
    # -----------------------------------------------------------------------
    def __init__(self, label=dict(), scale=100, show=False):
            
        self._scale = scale
        self._label = label
        self._show = show

    def get_load_img(self):
        return lambda path, spec: rsvis.utils.imgio(
            path,
            spec,
            param_label=self._label,
            scale=self._scale,
            show=self._show
        )